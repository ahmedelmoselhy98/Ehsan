public class AssociationModel {
    private String associationName;
    private String associationPassword;
    private String associationEmail;
    private String associationProfileImage;
    private String associationAuthorizationNumber;
    private String associationAuthorizationNumberImage;


    public AssociationModel() {
    }

    public String getAssociationName() {
        return associationName;
    }

    public void setAssociationName(String associationName) {
        this.associationName = associationName;
    }

    public String getAssociationPassword() {
        return associationPassword;
    }

    public void setAssociationPassword(String associationPassword) {
        this.associationPassword = associationPassword;
    }

    public String getAssociationEmail() {
        return associationEmail;
    }

    public void setAssociationEmail(String associationEmail) {
        this.associationEmail = associationEmail;
    }

    public String getAssociationProfileImage() {
        return associationProfileImage;
    }

    public void setAssociationProfileImage(String associationProfileImage) {
        this.associationProfileImage = associationProfileImage;
    }

    public String getAssociationAuthorizationNumber() {
        return associationAuthorizationNumber;
    }

    public void setAssociationAuthorizationNumber(String associationAuthorizationNumber) {
        this.associationAuthorizationNumber = associationAuthorizationNumber;
    }

    public String getAssociationAuthorizationNumberImage() {
        return associationAuthorizationNumberImage;
    }

    public void setAssociationAuthorizationNumberImage(String associationAuthorizationNumberImage) {
        this.associationAuthorizationNumberImage = associationAuthorizationNumberImage;
    }
}
